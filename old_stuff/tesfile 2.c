#include "JoystickDriver.c"  //Include file to "handle" the Bluetooth messages.
#include "ButtonsDefs.h"
bool isholding = false;

void	 initializeRobot()
{
	bFloatDuringInactiveMotorPWM = false;

	return;
}


task main()
{
	initializeRobot();

	waitForStart();   // wait for start of tele-op phase
	servo[holdleft] = 0;
servo[holdright] = 255;
	int x, y;
	float speed = 0.75;

	while (true)
	{
		getJoystickSettings(joystick);



		if(joystick.joy1_Buttons == button_right_trigger){

			motor[fr] = -100*speed;
			motor[fl] = 100*speed;
			motor[bl] = 100*speed ;
			motor[br] = -100*speed;
		}

		else if(joystick.joy1_Buttons == button_left_trigger){

			motor[fr] = 100*speed;
			motor[fl] = -100*speed;
			motor[bl] = -100*speed ;
			motor[br] = 100*speed;
		}
		else{
			x = joystick.joy1_x1 / 128.0 * 100 * speed;
			y = joystick.joy1_y1 / 128.0 * 100 * speed;

		  motor[bl] = (-x + y);
			motor[br] = (x + y);
			motor[fr] = (-x + y);
			motor[fl] = (x + y);
		}

		if(joystick.joy1_TopHat == pov_west){
			servo[holdleft] = 255;
servo[holdright] = 0;

	}

	if(joystick.joy1_TopHat == pov_east){

servo[holdleft] = 0;
servo[holdright] = 255;

}

if(joystick.joy1_TopHat == pov_north){

motor[shooterA] = -100;
motor[shooterB] = -100;
motor[sweep] = 100;
}
else if(joystick.joy1_TopHat == pov_south){

motor[shooterA] = 100;
motor[shooterB] = 100;
motor[sweep] = -100;

}
else{
motor[shooterA] = 0;
motor[shooterB] = 0;
motor[sweep] = 00;
}
if(joystick.joy1_TopHat == pov_east){

servo[holdleft] = 0;
servo[holdright] = 255;

}
	if(joystick.joy1_Buttons == button_x){
if(isholding == true){
	servo[holdleft] = 0;
servo[holdright] = 255;
isholding = false;
}
else{
servo[holdleft] = 255;
servo[holdright] = 0;
isholding = true;
}


}


		if(joystick.joy1_Buttons == button_left_button){
			speed = 0.45;
		}
		else if(joystick.joy1_Buttons == button_right_button){
			speed = 1.0;
		}
		else{
			speed = 0.75;
		}



	}
}
